class Patterntask{
    public static void main(String[] args) {
        for(int i=5;i>=1;i--){
            for(int j=1;j<=5;j++){
                System.out.print(i);
            }
                System.out.println("");
            }
        
         for(int i=5;i>=1;i--){
            for(int j=5;j>=1;j--){
                System.out.print(j);
            }
                System.out.println("");
            }

         int k = 1;
          for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                 System.out.print(k + " ");
                 k++;
            }
                 System.out.println("");
            }
        
        int g=1;
        for (int i = 1; i <= 5; i++) { 
             for (int j = 1; j <= 5; j++) { 
                 System.out.print(g+" ");
                   g+=2; 
                 }
                 System.out.println("");
            }
        }
}

